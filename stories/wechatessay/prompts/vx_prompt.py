def format_memories(memories: list) -> str:
    """将记忆列表格式化为字符串"""
    if not memories:
        return "（暂无用户习惯记忆）"
    return "\n".join([f"- {m}" for m in memories])


def format_revision_history(history: list) -> str:
    """将修改历史格式化为字符串"""
    if not history:
        return "（暂无修改历史）"
    return "\n".join([f"第{i+1}轮: {h}" for i, h in enumerate(history)])


