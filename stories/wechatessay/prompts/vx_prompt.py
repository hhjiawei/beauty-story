def get_json_schema_prompt(model_class) -> str:
    """
    从 Pydantic 模型生成 JSON schema 提示

    Args:
        model_class: Pydantic 模型类

    Returns:
        JSON schema 字符串
    """
    import json
    schema = model_class.model_json_schema()
    return json.dumps(schema, ensure_ascii=False, indent=2)
