# romanticstory/main.py
from romanticstory.graph.workflow import create_workflow
from romanticstory.states.romantic_story_state import MainState


def main():
    app = create_workflow()

    initial_state: MainState = {
        "user_input": """我叫沈知微，今年二十七岁，在这座城市经营一家小小的旧书店。店开在青石板巷深处，门口种着一棵老梧桐，秋天落叶的时候，整条巷子都是金黄的。
【初遇】
去年十月的某个雨天，他推门进来避雨。
他穿着深灰色的风衣，肩头沾着雨水，手里拿着一本湿透了的《边城》。他说想换一本干燥的。我递给他毛巾，他道谢时眼睛弯起来，像藏着一整个江南的烟雨。
他叫顾言声，三十二岁，是市立医院的心外科医生。从那天起，他每周三下午都会来，从不买书，只是坐在靠窗的旧藤椅上读一会儿书，喝一杯我泡的粗茶。
我们聊沈从文，聊汪曾祺，聊这座小城里哪家馄饨铺的虾皮最新鲜。他话不多，但每句话都让人觉得被认真对待。我开始期待每个周三，开始在他来之前换上那件浅蓝色的毛衣——虽然我从没告诉过他。
【相知】
十一月的一个傍晚，他来得比平时晚，脸色苍白。我问他怎么了，他说刚做完一台八小时的手术，病人没挺过来。那是他老师，也是带他入行的前辈。
我关了店门，陪他在梧桐树下站了很久。秋风吹过来，他忽然说："知微，我有时候觉得，我们抢救的不是生命，只是时间。"
我没有回答，只是把手里的热茶塞给他。他的手很凉，接过杯子时，指尖碰到了我的手背。那一秒钟，我觉得整条巷子的梧桐叶都在往下落。
那天晚上，他在书店待到打烊。临走时，他说："下周三我可能来不了，要去上海开会。"
我说："好。"
他走到门口，又回头看我："知微，等回来，我有话想跟你说。"
【变故】
他没有回来。
不是没从上海回来——他回来了，直接进了ICU。同事打电话来的时候，我正在给那盆他夸过"长得好"的绿萝浇水。急性心肌梗死，倒在手术台旁。
我赶到医院时，他已经不能说话了。我握着他的手，那双手曾经接过我递的茶，曾经翻过我店里的旧书，曾经差一点就要握住我的手。现在它们插满了管子，冰凉得像那个十一月的傍晚。
他的眼睛还能动。他看着我，好像有很多话要说。我把耳朵凑近他的嘴唇，听见他用了全部的力气，说了一个字："等……"
然后监护仪拉成直线。
【结局】
那是去年十二月的事。现在又是秋天了。
他的同事后来告诉我，上海会议结束后，他去旧货市场淘了一整天，想找一本1948年版的《边城》——我曾在店里提过，说那是我的执念，可惜一直寻不到。
那本书现在在我的收银台下面。扉页上有他写的字，笔迹因为病痛已经歪斜："愿知微余生有雨声相伴，不再等。"
我每周三还是会泡一杯粗茶，放在靠窗的旧藤椅上。梧桐叶又黄了，落在青石板上，被雨水泡得发亮。
有时候我会想，如果那个十一月的傍晚，我主动握住他的手，故事会不会不一样。但医生朋友说，他的心脏问题由来已久，那次发作只是时间问题。
时间从来不等人。而我们总以为还有很多时间。
书店还在开着。有客人问我，为什么总在周三下午对着空椅子发呆。我说，我在等一个人。
他们问，等谁？
我说，等一个已经来过的人。
雨又下了起来，整条巷子都是雨声。我忽然想起他最后说的那个字——不是"等"，是"灯"。他大概是想说，让我记得关好店里的灯。
或者，他是想说，他愿意做我余生的一盏灯。
只是雨声太大，我听不清了。""",
        "plan_state": {},
        "character_state": {},
        "plot_state": {},
        "current_segment_index": 0,
        "segments": [],
        "current_polish_index": 0  # ✅ 新增：抛光索引初始化为 0
    }

    print("🚀 开始生成故事...")

    # 运行工作流
    final_state = None
    for event in app.stream(initial_state):
        for node_name, output in event.items():
            print(f"\n✅ 节点 [{node_name}] 处理完毕")
            final_state = output

    print("\n" + "=" * 60)
    print("🎉 故事生成完毕！")
    print("=" * 60)

    # 打印完整故事
    if final_state and "segments" in final_state:
        print("\n" + "=" * 60)
        print("📖 完整故事")
        print("=" * 60)

        full_story = ""
        for seg in final_state.get("segments", []):
            print(f"\n{'=' * 20} {seg['para_id']} {'=' * 20}")
            print(seg['content'])
            full_story += f"\n\n【{seg['para_id']}】\n{seg['content']}"

        print("\n" + "=" * 60)
        print(f"📊 完整故事总字数：{len(full_story)} 字符")
        print("=" * 60)


if __name__ == "__main__":
    main()
